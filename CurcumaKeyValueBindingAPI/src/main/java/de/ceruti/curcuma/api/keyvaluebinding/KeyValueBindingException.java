package de.ceruti.curcuma.api.keyvaluebinding;

public class KeyValueBindingException extends RuntimeException {

	public KeyValueBindingException() {
	}

	public KeyValueBindingException(String arg0) {
		super(arg0);
	}

	public KeyValueBindingException(Throwable arg0) {
		super(arg0);
	}

	public KeyValueBindingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
