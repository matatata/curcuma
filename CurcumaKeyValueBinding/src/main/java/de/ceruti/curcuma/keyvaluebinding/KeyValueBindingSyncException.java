package de.ceruti.curcuma.keyvaluebinding;

public class KeyValueBindingSyncException extends Exception {

	public KeyValueBindingSyncException() {
	}

	public KeyValueBindingSyncException(String arg0) {
		super(arg0);
	}

	public KeyValueBindingSyncException(Throwable arg0) {
		super(arg0);
	}

	public KeyValueBindingSyncException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
